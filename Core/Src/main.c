void set_motor_pwm(uint8_t duty_percent)
{
  if (duty_percent > 100) duty_percent = 100;
  uint32_t pwm_value = ((htim3.Init.Period + 1) * duty_percent) / 100;
  __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, pwm_value);
}

void motor_stop(void)
{
  set_motor_pwm(0);
  HAL_GPIO_WritePin(MOTOR_IN_GPIO_PORT, MOTOR_IN1_PIN | MOTOR_IN2_PIN, GPIO_PIN_RESET);
}

void motor_forward(void)
{
  HAL_GPIO_WritePin(MOTOR_IN_GPIO_PORT, MOTOR_IN1_PIN, GPIO_PIN_SET);
  HAL_GPIO_WritePin(MOTOR_IN_GPIO_PORT, MOTOR_IN2_PIN, GPIO_PIN_RESET);
}

